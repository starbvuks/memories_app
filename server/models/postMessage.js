import mongoose from "mongoose";

// Schema for posts
const postSchema = mongoose.Schema({
  title: String,
  mesage: String,
  creator: String,
  tags: [String],
  selectedFile: String,
  likeCount: {
    type: Number,
    default: 0,
  },
  createdAt: {
    type: Date,
    default: new Date(),
  },
});

const PostMessage = mongoose.model("PostMessage", postSchema);
